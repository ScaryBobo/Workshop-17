package VTTP.Workshop17.Model;

public class ConversionPair {
    
    private String id;
    private String fr;
    private String to;
    private double val;

    
    public String getId() {
        return id;
    }
    public void setId(String id) {
        this.id = id;
    }
    public String getFr() {
        return fr;
    }
    public void setFr(String fr) {
        this.fr = fr;
    }
    public String getTo() {
        return to;
    }
    public void setTo(String to) {
        this.to = to;
    }
    public double getVal() {
        return val;
    }
    public void setVal(double val) {
        this.val = val;
    }

    
    
}
